package NSAA___Loops;

import java.util.*;
public class Qn_21 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the value n: ");
		int n = sc.nextInt();
		for(int i=1; i<=n; i++) {
			System.out.println(i);
		}
		sc.close();
	}
}